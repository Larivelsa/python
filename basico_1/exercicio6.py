'''
O código abaixo verifica se de acordo com o valor informado, 
você tem a idade mínima para ser presidente do país. Complete as 
lacunas para que o código fique completo.

OBS.: A idade mínima para ser presidente é de 35 anos

'''

idade = int(input('Digite sua idade: '))

if idade >= 35:
    print("Você tem idade para ser presidente")
else:
    print("Você não tem idade para ser presidente")